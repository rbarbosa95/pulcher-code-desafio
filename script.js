function randomDate(date1, date2) {
  function randomValueBetween(min, max) {
    return Math.random() * (max - min) + min
  }
  var date1 = date1 || '01-01-1970'
  var date2 = date2 || new Date().toLocaleDateString()
  date1 = new Date(date1).getTime()
  date2 = new Date(date2).getTime()
  if (date1 > date2) {
    return new Date(randomValueBetween(date2, date1)).toLocaleDateString()
  } else {
    return new Date(randomValueBetween(date1, date2)).toLocaleDateString()
  }
}

const mediaTypes = ['Digital', 'Physical']

const parsedList = gameTitles.map((title, index) => {
  return {
    id: index + 1,
    name: title,
    purchase_date: new Date(randomDate('01/01/2010', '12/31/2023')).toISOString(),
    media_type: mediaTypes[Math.round(Math.random())],
    store: Math.floor(Math.random() * 4) + 1,
    value: Number((Math.random() * 300 + 10).toFixed(2))
  }
})

const gen = async () => {
  await appendFile('./db.json', JSON.stringify(parsedList, null, 2), { encoding: 'utf8' })
}

gen()
